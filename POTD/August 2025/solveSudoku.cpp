bool isValidSudoku(vector<vector<char>> &board)
{
    vector<set<char>> rows(9), cols(9), blocks(9);

    for (int i = 0; i < 9; i++)
    {
        for (int j = 0; j < 9; j++)
        {
            char c = board[i][j];
            if (c == '.')
                continue; // skip empty cells

            // Check row
            if (rows[i].count(c))
                return false;
            rows[i].insert(c);

            // Check column
            if (cols[j].count(c))
                return false;
            cols[j].insert(c);

            // Check 3x3 block
            int blockIndex = (i / 3) * 3 + (j / 3);
            if (blocks[blockIndex].count(c))
                return false;
            blocks[blockIndex].insert(c);
        }
    }

    return true;
}
